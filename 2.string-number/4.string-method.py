
# string methods
print("hello world".upper())
print("HELLO WORLD".lower())
print("hello world".capitalize())
print("hello world".title())
print("hello world".count("l")) # count the number of times a character appears in a string
print("hello world".find("l")) # find the index of a character in a string
print("hello world".replace("l", "L"))
print("hello world".split(" "))
print("hello world".split("l"))
print("hello world".split("l", 1)) # split the string at the first occurrence of the character


